    public void setValueTable(){
        //List<Funcionario> fun = FC.buscarNomeParcial(nome);
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        Object rowData[] = new Object[8];
        model.setRowCount(0);
        for(int i =0; i< V.size(); i++){
            rowData[0] = V.get(i).getCodBilhete();
            rowData[1] = V.get(i).getServico();
            rowData[2] = V.get(i).getNomeCliente();
            rowData[3] = V.get(i).getRg();
            rowData[4] = V.get(i).getCpfCnpj();
            rowData[5] = V.get(i).getDataVenda();
            rowData[6] = V.get(i).getHoraVenda();
            rowData[7] = V.get(i).getNumPoltrona();
            model.addRow(rowData);
        }
    }

