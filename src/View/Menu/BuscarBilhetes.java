    private void btConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarActionPerformed
        Date dataCompra = jDateChooserCompra.getDate();
        String dCompra = sdf.format(dataCompra);
        System.out.println("____________________________________");

        try{
            if (jDateChooserCompra.getDate() == null) {
                System.out.println("1");
                JOptionPane.showMessageDialog(null , "Insira uma data de viagem ou de compra.", "Erro", JOptionPane.INFORMATION_MESSAGE);            
                jDateChooserCompra.grabFocus();
            }else{
                int codBilhete;
                String nome;
                System.out.println("2");
                if(!txtCodBilhete.getText().trim().equals("") && !txtNome.getText().trim().equalsIgnoreCase("")){
                    System.out.println("4");
                    codBilhete = Integer.parseInt(txtCodBilhete.getText());
                    nome = txtNome.getText();
                    V = VC.buscarServico(dCompra, codBilhete, nome);
                    setButtonOn();
                }else if(!txtCodBilhete.getText().trim().equals("") && txtNome.getText().trim().equals("")){
                    codBilhete = Integer.parseInt(txtCodBilhete.getText());
                    System.out.println("5");
                    V = VC.buscarServico(dCompra, codBilhete);
                    setButtonOn();
                }else if(txtCodBilhete.getText().trim().equals("") && !txtNome.getText().trim().equalsIgnoreCase("")){
                    nome = txtNome.getText();
                    System.out.println("6");
                    V = VC.buscarServico(dCompra, nome);
                    setButtonOn();
                }else{
                    System.out.println("fim");
                    JOptionPane.showMessageDialog(null , "Digite o Nome/Bilhete!!!", "Erro", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }catch(NumberFormatException ex){
            System.out.println("Erro: " + ex);
        }
    }//GEN-LAST:event_btConfirmarActionPerformed

    private void btImprimir2ViaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btImprimir2ViaActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        int linhaSelec = jTable.getSelectedRow();
        if(jTable.getSelectedRow() == -1){
            if(jTable.getSelectedRowCount() == 0){
                JOptionPane.showMessageDialog(null, "Tabela está vazia", "Não Encontrado", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um Perfil !!!", "Não Encontrado", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            String nome = model.getValueAt(linhaSelec, 2).toString();
            int polt = Integer.parseInt(model.getValueAt(linhaSelec, 7).toString());
            int serv = Integer.parseInt(model.getValueAt(linhaSelec, 1).toString());
            String dataAt = model.getValueAt(linhaSelec, 5).toString();
            String hourAt = model.getValueAt(linhaSelec, 6).toString();
            B = BC.getDadosBilhete(nome, polt, serv, dataAt, hourAt);
            System.out.println("------------");

            BC.createPdfFile(B);
        }        
    }//GEN-LAST:event_btImprimir2ViaActionPerformed

