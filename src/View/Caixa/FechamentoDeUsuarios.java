    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        try{
            System.out.println("00");
            String dataDeBusca = sdf.format(jDateChooser.getDate());
            System.out.println(dataDeBusca);
            String nomeBusca = txtNomeLogin.getText().toUpperCase();
            System.out.println("01");
            if(dataDeBusca.trim().equals("") || nomeBusca.trim().equals("")){
                this.toBack();
                JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }else{
                Connection conn = ConnectionFactory.getConnection();
                System.out.println("000");
                try{
                    System.out.println("001");
                    Funcionario f;
                    FuncionarioControlador fc = new FuncionarioControlador();
                    f = fc.buscar(nomeBusca);
                    int codFuncionario = f.getCodigo();
                    System.out.println("dataSessao: " + dataDeBusca);
                    System.out.println("codFuncionario: " + codFuncionario);
                    Map<String, Object> parametros = new HashMap<>();
                    parametros.put("dataSessao", dataDeBusca);
                    parametros.put("codFuncionario", codFuncionario);
                    this.toBack();
                    JasperPrint jp = JasperFillManager.fillReport("src/Reports/iReportSellsByDate.jasper", parametros, conn);
                    JasperViewer.viewReport(jp, false);
                }catch(JRException ex){
                    System.out.println("Erro ex: " + ex);
                    Logger.getLogger(FechamentoDeUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }catch(HeadlessException ex){
            System.out.println("Erro: " + ex);
        }
    }//GEN-LAST:event_jBuscarActionPerformed

