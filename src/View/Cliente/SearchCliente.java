    private void btEditProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditProfileActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        int linhaSelec = jTable.getSelectedRow();
        if(jTable.getSelectedRow() == -1){
            if(jTable.getSelectedRowCount() == 0){
                JOptionPane.showMessageDialog(null, "Tabela está vazia", "Não Encontrado", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um Perfil !!!", "Não Encontrado", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            UpdCliente form = new UpdCliente(null, true);
            String idEsc = model.getValueAt(linhaSelec, 0).toString();
            form.valorId = Integer.parseInt(idEsc);
            form.usuario = F.getLogin();
            form.setValores();
            this.dispose();
            form.setLocationRelativeTo(null);
            form.setVisible(true);
        }
    }//GEN-LAST:event_btEditProfileActionPerformed

    private void btDeleteProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteProfileActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        int linhaSelec = jTable.getSelectedRow();
        if(jTable.getSelectedRow() == -1){
            if(jTable.getSelectedRowCount() == 0){
                JOptionPane.showMessageDialog(null, "TABELA ESTÁ VAZIA", "NÃO ENCONTRADO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "SELECIONE UM PERFIL !!!", "NÃO ENCONTRADO", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            int i = JOptionPane.showConfirmDialog(null, "Deseja Realmente Ecluir Registro de Cliente?", 
                    "Exclusão", JOptionPane.YES_NO_OPTION);
            if(i == JOptionPane.YES_OPTION){
                String idExc = model.getValueAt(linhaSelec, 0).toString();
                cc.excluir(Integer.valueOf(idExc));
                refresh();
            }else if(i == JOptionPane.NO_OPTION){
                JOptionPane.showMessageDialog(null, "EXCLUSÃO CANCELADA", "FECHAR", JOptionPane.CLOSED_OPTION);
            }
        }
    }//GEN-LAST:event_btDeleteProfileActionPerformed

