    private void btBuscarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarNomeActionPerformed
        String nome = txtBuscarNome.getText();
        System.out.println("NOME: "+nome);
        if(nome != null && !nome.isEmpty()){
            if(txtDoc.getText().trim().equals("")){
                if(nome.length() >= 3){
                    List<Cliente> cli = cc.buscarNomeParcial(nome);
                    DefaultTableModel model = (DefaultTableModel) jTable.getModel();
                    Object rowData[] = new Object[5];
                    model.setRowCount(0);
                    for(int i =0; i< cli.size(); i++){
                        rowData[0] = cli.get(i).getId();
                        rowData[1] = cli.get(i).getNome();
                        rowData[2] = cli.get(i).getRg();
                        rowData[3] = cli.get(i).getCpf_cnpj();
                        rowData[4] = cli.get(i).getTelefone();
                        model.addRow(rowData);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Nome Curto Para Realizar Busca !!!", "Erro", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                String cpf = txtDoc.getText();
                if(nome.length() >= 3){
                    List<Cliente> cli = cc.buscarNomeEDoc(nome, cpf);
                    DefaultTableModel model = (DefaultTableModel) jTable.getModel();
                    Object rowData[] = new Object[5];
                    model.setRowCount(0);
                    for(int i =0; i< cli.size(); i++){
                        rowData[0] = cli.get(i).getId();
                        rowData[1] = cli.get(i).getNome();
                        rowData[2] = cli.get(i).getRg();
                        rowData[3] = cli.get(i).getCpf_cnpj();
                        rowData[4] = cli.get(i).getTelefone();
                        model.addRow(rowData);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Nome Curto Para Realizar Busca !!!", "Erro", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Digite os campos da busca", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btBuscarNomeActionPerformed

